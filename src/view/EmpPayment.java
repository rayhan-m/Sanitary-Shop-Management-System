    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        if(cmbEmpID.getSelectedItem().toString().equals("Select One")){
            JOptionPane.showMessageDialog(rootPane, "Please Select one Employee ID");
        }else{
        
        String cmbEID=cmbEmpID.getSelectedItem().toString();
        
        
        emppay=controller.searchPayInfo(cmbEID);
        if(emppay!=null){
            txtSalary.setText(emppay.getSalary()+"");
            cmbPayMonth.setSelectedItem(emppay.getPaymentMonth()+"");
            cmbStatus.setSelectedItem(emppay.getStatus()+"");
        }
        else{
                JOptionPane.showMessageDialog(null, "No Data Found");
            }
        }

    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            if(cmbEmpID.getSelectedItem().toString().equals("Select One")){
                 JOptionPane.showMessageDialog(null, "Select Employee ID");
            }else{
//              emppay.setEmpId(cmbEmpID.getSelectedIndex());
            emppay.setEmpId(Integer.parseInt(cmbEmpID.getSelectedItem().toString()));
            emppay.setSalary(Double.parseDouble(txtSalary.getText()));
            
            DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
//            String date= dateFormat.format(txtDateChoser.getDate());
            emppay.setDate(dateFormat.format(txtDateChoser.getDate()));
//            emppay.setDate(txtDateChoser.getDate().toString());
            emppay.setPaymentMonth(cmbPayMonth.getSelectedItem().toString());
            emppay.setStatus(cmbStatus.getSelectedItem().toString());
            

                
                    controller.updatePayment(emppay);
                } 
            } catch (SQLException ex) {
            Logger.getLogger(EmpPayment.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

