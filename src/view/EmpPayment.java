    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
    if(cmbEmpID.getSelectedItem().toString().equals("Select One")){
            JOptionPane.showMessageDialog(rootPane, "Please Select one Employee ID");
        }
    else if(txtSalary.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please Enter Salary");
            
        }
    else if(txtDateChoser.getDate().toString().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please Select Payment Date");
        }
    else if(cmbPayMonth.getSelectedItem().toString().equals("Select One")){
            JOptionPane.showMessageDialog(rootPane, "Please Select Payment Month ");
        }
    else if(cmbStatus.getSelectedItem().toString().equals("Select Status")){
            JOptionPane.showMessageDialog(rootPane, "Please Select Status");
        }
    
    else{
            
            String empId = this.cmbEmpID.getSelectedItem().toString();
            String salary=txtSalary.getText();
            DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            String date= dateFormat.format(txtDateChoser.getDate());
            String paymentMonth=cmbPayMonth.getSelectedItem().toString();
            String status=cmbStatus.getSelectedItem().toString();
            
            EmpPaymentInfo empPay=new EmpPaymentInfo(ERROR, ABORT, date.toString(), paymentMonth, status.toString());
//            EmpPaymentInfo empPay=new EmpPaymentInfo(ERROR, 0, date.toString(), status.toString());
            empPay.empId=Integer.parseInt(empId);
            empPay.salary=Double.parseDouble(salary);
            controller.insertPayment(empPay);
        }
    }//GEN-LAST:event_btnAddActionPerformed

